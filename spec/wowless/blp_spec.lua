describe('blp', function()
  it('reads', function()
    local pf = require('pl.file')
    local w, h, rgba = require('wowless.blp').read(pf.read('spec/wowless/temp.blp'))
    assert.same(w, 64)
    assert.same(h, 64)
    assert.same(pf.read('spec/wowless/temp.png'), require('wowless.png').write(w, h, rgba))
  end)
end)
